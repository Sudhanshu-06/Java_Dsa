package Loops;

import java.util.Scanner;

public class PrintNTimes {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter  the no of times:- ");
        int n = sc.nextInt();
        for(int i= 1; i<=n; i++){
            System.out.print(i+" ");
        }
    }
}
