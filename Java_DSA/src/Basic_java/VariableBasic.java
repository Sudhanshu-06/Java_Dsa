package Basic_java;

public class VariableBasic {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        System.out.println(x+5);
        System.out.println(x+"Sudhanshu");
        int y;   //declaration
        y = 20;  //initialization
        y = y+5;
        System.out.println(y);



        int a = 7;
        System.out.println(a);
        a = a - 2;
        System.out.println(a);
        a = a + 20;
        System.out.println(a);
        a = a - 30;
        System.out.println(a);
        a = a * 10;
        System.out.println(a);
        a = a / 25 ;
        System.out.println(a);
    }
}
